# STREAMING FACTOR GENERATION — on-demand D_L / D_A from (raw_mp4, mask) pairs
# ═════════════════════════════════════════════════════════════════════════
# Purpose: eliminate m11's ~340 GB disk write at 10K / ~4 TB at 115K by
# generating D_L / D_A inside DataLoader workers. Keeps m10 masks + raw MP4s
# on disk; factor arrays materialize only in worker RAM, feed directly to
# student forward. Unlocks full 10K → 50K → 115K ladder on a single 500 GB
# instance (vs 500 GB → 3 TB → 5 TB rent ladder).
#
# Contract (technique-agnostic):
#   - StreamingFactorDataset takes (mp4_index, mask_index, factor_cfg,
#     mode_mixture, ...) as explicit params. NO argparse/technique branching.
#   - D_L / D_A stream via utils.factor_streaming.stream_factor; D_I stays
#     on legacy disk path (Stage 3 retired in iter9's 2-stage recipe).
#   - Worker init forces torch.set_num_threads(1) + PyAV thread_count=1 (R2
#     in plan_code_dev.md risk register).
#
# Bitwise parity with legacy .npy: guaranteed by (a) importing m11's
# make_layout_only/make_agent_only directly, (b) mirroring m11:652-664 mask
# alignment, (c) sharing tensor_from_factor_array normalization with
# load_factor_clip.

def build_streaming_indices(manifest_path: Path, masks_dir: Path, local_data: str) -> tuple:
    """Map clip_keys → (TAR path, base name) and mask .npz paths.

    One-shot scan of the local TAR pool in the main process. Called once at
    stage setup, passed to DataLoader workers via dataset __init__ — workers
    re-open TARs locally (fork-safe), never sharing iter_clips_parallel's
    threads.

    Args:
        manifest_path: path to m11 factor_manifest.json
        masks_dir: directory holding m10 .npz mask files
        local_data: path to local_data dir with *.tar shards

    Returns:
        (mp4_index, mask_index, factor_manifest)
            mp4_index: {clip_key: (tar_path: str, base_name: str)}
            mask_index: {clip_key: npz_path: Path}
            factor_manifest: loaded m11 manifest dict (for has_D_L/has_D_A gating)
    """
    import tarfile
    factor_manifest = json.loads(Path(manifest_path).read_text())
    manifest_keys = set(factor_manifest.keys())

    tar_files = sorted(Path(local_data).glob("*.tar"))
    if not tar_files:
        raise FileNotFoundError(
            f"build_streaming_indices: no *.tar files in {local_data}. "
            f"Did you run ./git_pull.sh to fetch local_data?")

    mp4_index = {}
    from utils.data_download import _get_clip_key
    for tar_path in tar_files:
        with tarfile.open(tar_path, "r") as tar:
            entries = {}
            for member in tar.getmembers():
                base = member.name.rsplit(".", 1)[0]
                ext = member.name.rsplit(".", 1)[-1] if "." in member.name else ""
                entries.setdefault(base, {})[ext] = member
            for base, parts in entries.items():
                if "json" not in parts or "mp4" not in parts:
                    continue
                json_bytes = tar.extractfile(parts["json"]).read()
                clip_key = _get_clip_key(json_bytes)
                if clip_key in manifest_keys and clip_key not in mp4_index:
                    mp4_index[clip_key] = (str(tar_path), base)

    mask_index = {}
    masks_dir = Path(masks_dir)
    for clip_key in manifest_keys:
        safe_key = clip_key.replace("/", "__")
        npz_path = masks_dir / f"{safe_key}.npz"
        if npz_path.exists():
            mask_index[clip_key] = npz_path

    indexed = set(mp4_index.keys()) & set(mask_index.keys())
    dropped = manifest_keys - indexed
    if dropped:
        print(f"  build_streaming_indices: {len(dropped)} manifest clips missing "
              f"MP4 or mask — excluded. Sample: {list(dropped)[:3]}")
    print(f"  build_streaming_indices: {len(indexed)} clips ready "
          f"({len(mp4_index)} mp4, {len(mask_index)} masks, {len(tar_files)} TARs scanned)")
    return mp4_index, mask_index, factor_manifest


def _streaming_worker_init(worker_id: int) -> None:
    """DataLoader worker_init_fn — single-threaded CPU + seeded RNG.

    CRITICAL (risk R2): without thread_count=1 on PyAV codecs, each worker
    spawns ffmpeg threads → host OOM at num_workers=16. See src/CLAUDE.md
    'Threading' rule.
    """
    info = torch.utils.data.get_worker_info()
    dataset = info.dataset
    seed = ((dataset.base_seed * 1_000_003) ^ (worker_id * 2_718_281)) & 0xFFFFFFFF
    np.random.seed(seed)
    random.seed(seed)
    torch.manual_seed(seed)
    torch.set_num_threads(1)
    try:
        import av
        av.logging.set_level(av.logging.ERROR)
    except ImportError:
        print(f"  worker {worker_id}: PyAV not importable — thread_count not capped")


class StreamingFactorDataset(torch.utils.data.IterableDataset):
    """On-demand D_L / D_A / D_I factor generator for DataLoader.

    Replaces synchronous FactorSampler.sample() + load_factor_clip() with a
    DataLoader-driven pipeline. Each worker owns a shard of clip_keys, samples
    (factor_type, clip_key) per mode_mixture weights, decodes MP4 + mask from
    disk, runs stream_factor() (D_L/D_A) or stream_interaction_tubes() (D_I)
    → normalized tensor.

    Yields dicts with keys {"tensor", "factor_type", "clip_key"} — default
    PyTorch collate stacks tensors, lists strings.

    D_I streaming (iter10 2026-04-22): tubes generated on-demand from
    (mp4_bytes, mask.npz), no disk reads. `di_legacy_index` param retained
    for backward-compat but unused when `interaction_cfg` is passed.
    """

    def __init__(
        self,
        mp4_index: dict,
        mask_index: dict,
        factor_manifest: dict,
        factor_cfg: dict,
        mode_mixture: dict,
        num_frames: int,
        crop_size: int,
        di_legacy_index: dict = None,
        base_seed: int = 42,
        steps_per_epoch: int = None,
        interaction_cfg: dict = None,
    ):
        super().__init__()
        self.mp4_index = mp4_index
        self.mask_index = mask_index
        self.factor_cfg = factor_cfg
        self.num_frames = num_frames
        self.crop_size = crop_size
        self.di_legacy_index = di_legacy_index or {}
        self.interaction_cfg = interaction_cfg
        self.base_seed = base_seed
        self.steps_per_epoch = steps_per_epoch

        factor_map = {"L": "D_L", "A": "D_A", "I": "D_I"}
        indexed_keys = set(mp4_index.keys()) & set(mask_index.keys())
        self.clips_by_factor = {"D_L": [], "D_A": [], "D_I": []}
        for clip_key in indexed_keys:
            info = factor_manifest.get(clip_key, {})
            if info.get("has_D_L"):
                self.clips_by_factor["D_L"].append(clip_key)
            if info.get("has_D_A"):
                self.clips_by_factor["D_A"].append(clip_key)
            # iter10: D_I streaming — manifest's has_D_I (populated from m10
            # mined interactions count) replaces legacy .npy disk presence.
            if info.get("has_D_I"):
                self.clips_by_factor["D_I"].append(clip_key)
        # Back-compat: if legacy disk tubes were provided (pre-iter10) and
        # manifest lacks has_D_I, still populate from legacy index.
        if not self.clips_by_factor["D_I"] and self.di_legacy_index:
            for clip_key, tubes in self.di_legacy_index.items():
                if tubes:
                    self.clips_by_factor["D_I"].append(clip_key)

        self.factors = []
        self.weights = []
        for mode_key, weight in mode_mixture.items():
            if weight <= 0:
                continue
            fk = factor_map[mode_key]
            if not self.clips_by_factor[fk]:
                counts = {k: len(v) for k, v in self.clips_by_factor.items()}
                raise ValueError(
                    f"StreamingFactorDataset: mode_mixture requests {mode_key}={weight} "
                    f"but clips_by_factor[{fk}] is empty (counts={counts}). Silent "
                    f"renormalization would run a different distribution than yaml — "
                    f"failing loud per CLAUDE.md §5. Fix: upstream (e.g., re-run m10 "
                    f"--interactions-only to populate n_interactions for D_I) OR set "
                    f"{mode_key}=0.0 in yaml mode_mixture."
                )
            self.factors.append(fk)
            self.weights.append(weight)
        if not self.factors:
            raise ValueError(
                f"StreamingFactorDataset: no positive weights in mode_mixture={mode_mixture}"
            )
        total_w = sum(self.weights)
        self.weights = [w / total_w for w in self.weights]
        requested = {k: v for k, v in mode_mixture.items() if v > 0}
        actual = dict(zip(self.factors, self.weights))
        print(
            f"  StreamingFactorDataset: REQUESTED={requested} → "
            f"ACTUAL={{{', '.join(f'{f}={w:.0%}' for f, w in actual.items())}}} "
            f"(indexed={len(indexed_keys)} clips)"
        )

    def __iter__(self):
        from utils.factor_streaming import (
            stream_factor, stream_interaction_tubes, tensor_from_factor_array,
        )
        import tarfile

        info = torch.utils.data.get_worker_info()
        if info is None:
            worker_id, num_workers = 0, 1
        else:
            worker_id, num_workers = info.id, info.num_workers

        rng = np.random.default_rng(
            ((self.base_seed * 1_000_003) ^ (worker_id * 2_718_281)) & 0xFFFFFFFF)

        per_worker = {}
        for fk, keys in self.clips_by_factor.items():
            per_worker[fk] = keys[worker_id::num_workers] if keys else []
        active_factors = [f for f in self.factors if per_worker[f]]
        if not active_factors:
            return
        active_weights = np.array(
            [self.weights[self.factors.index(f)] for f in active_factors], dtype=np.float64)
        active_weights /= active_weights.sum()

        tar_cache = {}

        def _get_mp4_bytes(clip_key):
            tar_path, base = self.mp4_index[clip_key]
            if tar_path not in tar_cache:
                tar_cache[tar_path] = tarfile.open(tar_path, "r")
            tar = tar_cache[tar_path]
            mp4_member = tar.getmember(f"{base}.mp4")
            return tar.extractfile(mp4_member).read()

        try:
            with tempfile.TemporaryDirectory(prefix="m09c_stream_") as tmp:
                n_emitted = 0
                while self.steps_per_epoch is None or n_emitted < self.steps_per_epoch:
                    factor_type = active_factors[
                        rng.choice(len(active_factors), p=active_weights)]
                    clip_key = per_worker[factor_type][
                        rng.integers(len(per_worker[factor_type]))]

                    if factor_type == "D_I":
                        # iter10: D_I streaming. Generate tubes on-demand from
                        # (mp4_bytes, mask.npz); falls back to legacy disk path
                        # when interaction_cfg not provided (pre-iter10 callers).
                        if self.interaction_cfg is not None:
                            mp4_bytes = _get_mp4_bytes(clip_key)
                            tubes = stream_interaction_tubes(
                                mp4_bytes=mp4_bytes,
                                mask_npz_path=self.mask_index[clip_key],
                                interaction_cfg=self.interaction_cfg,
                                num_frames=self.num_frames,
                                tmp_dir=tmp,
                                clip_key=clip_key,
                            )
                            if not tubes:
                                # Clip lost all tubes to blacklist — re-sample next step.
                                continue
                            frames_uint8 = tubes[rng.integers(len(tubes))]
                        else:
                            tubes = self.di_legacy_index[clip_key]
                            tube_path = tubes[rng.integers(len(tubes))]
                            frames_uint8 = np.load(tube_path)
                    else:
                        mp4_bytes = _get_mp4_bytes(clip_key)
                        frames_uint8 = stream_factor(
                            mp4_bytes=mp4_bytes,
                            mask_npz_path=self.mask_index[clip_key],
                            factor_type=factor_type,
                            factor_cfg=self.factor_cfg,
                            num_frames=self.num_frames,
                            tmp_dir=tmp,
                            clip_key=clip_key,
                        )

                    tensor = tensor_from_factor_array(
                        frames_uint8, self.num_frames, self.crop_size)
                    yield {
                        "tensor": tensor,
                        "factor_type": factor_type,
                        "clip_key": clip_key,
                    }
                    n_emitted += 1
        finally:
            for tar_path, tar in tar_cache.items():
                try:
                    tar.close()
                except (OSError, tarfile.TarError) as e:
                    print(f"  StreamingFactorDataset: error closing {tar_path}: {e}")


# ═════════════════════════════════════════════════════════════════════════
# MID-TRAINING PROBE EVAL (Prec@K / mAP@K / Cycle@K with BCa 95% CI)
# ═════════════════════════════════════════════════════════════════════════
#
# Purpose: at stage boundaries, embed a held-out probe set through the CURRENT
# student and compute the same retrieval metrics the D.4 decision gate uses.
# Watches Surgery > Frozen emerge (or not) across the 3 stages without waiting
# for the full m05+m06 re-run cycle.
#
# Contract (technique-agnostic):
#   - Input:  student (vjepa2 VisionTransformer), pre-decoded probe tensors,
#             cfg dict, device.
#   - Output: dict with {prec_at_k, map_at_k, cycle_at_k} each {mean, ci_half}
#             and num_clips.
#   - No persistent state mutation: train()/eval() + return_hierarchical toggled
#     then restored under try/finally so the stage loop continues cleanly.
#
# Pooling matches m05_vjepa_embed.py:242 (outputs.mean(dim=1) over patch dim).
# Deterministic preprocess (center-crop + ImageNet-norm, no aug) so mid-stage
# deltas track the encoder, not the augmentation RNG.


def _probe_preprocess(video_uint8: torch.Tensor, crop_size: int) -> torch.Tensor:
    """Deterministic probe pipeline: center-crop → resize → ImageNet-normalize.

    video_uint8: (T, C, H, W) uint8 from decode_video_bytes().
    Returns: (T, C, crop_size, crop_size) float32, ImageNet-normalized.
    """
    _, _, H, W = video_uint8.shape
    side = min(H, W)
    top = (H - side) // 2
    left = (W - side) // 2
    video = video_uint8[:, :, top:top + side, left:left + side].float() / 255.0
    video = F.interpolate(video, size=(crop_size, crop_size),
                          mode="bilinear", align_corners=False)
    return (video - IMAGENET_MEAN.to(video.device)) / IMAGENET_STD.to(video.device)


def build_probe_clips(probe_subset_path: str, probe_local_data: str,
                      probe_tags_path: str, num_frames: int, crop_size: int,
                      subset_keys_override: set = None) -> list:
    """Decode + preprocess probe clips ONCE upfront. Returns list of
    (clip_key, tag_dict, tensor[T,C,H,W]).

    Tag alignment: Path(clip_key).name == tag["source_file"]. Clips without a
    matching tag record are skipped (not fatal — probe is best-effort signal).
    Heavy: 1K val_1k clips take ~5-10 min to decode via PyAV. Cost amortized
    across multiple probe calls.

    subset_keys_override: if provided (e.g., m09c held-out 10% split), skip the
    JSON load and use these keys directly. Lets callers plumb an in-memory split
    without writing a temp JSON to disk.
    """
    if subset_keys_override is not None:
        subset_keys = set(subset_keys_override)
    else:
        subset_keys = set(json.load(open(probe_subset_path))["clip_keys"])
    tags_list = json.load(open(probe_tags_path))
    # Fail-loud: tag records without "source_file" are malformed — skip with explicit test, no .get default.
    tag_by_source = {t["source_file"]: t for t in tags_list if "source_file" in t}

    tmp_dir = tempfile.mkdtemp(prefix="probe_decode_")
    clips = []
    n_no_tag = 0
    try:
        clip_q, tar_stop, _reader = iter_clips_parallel(probe_local_data, subset_keys=subset_keys)
        while True:
            try:
                item = clip_q.get(timeout=120)
            except queue.Empty:
                break
            if item is None:
                break
            clip_key, mp4_bytes = item
            if not mp4_bytes:
                continue
            source_file = Path(clip_key).name
            if source_file not in tag_by_source:
                n_no_tag += 1
                continue
            tag = tag_by_source[source_file]
            video_u8 = decode_video_bytes(mp4_bytes, tmp_dir, clip_key, num_frames)
            if video_u8 is None:
                continue
            tensor = _probe_preprocess(video_u8, crop_size)
            clips.append((clip_key, tag, tensor))
        tar_stop.set()
    finally:
        shutil.rmtree(tmp_dir, ignore_errors=True)

    print(f"  [probe] decoded {len(clips)} clips ({n_no_tag} skipped — no tag match)")
    if len(clips) < 100:
        print(f"  WARNING: probe has only {len(clips)} clips — bootstrap CI will be wide (N<100)")
    return clips


@torch.no_grad()
def run_probe_eval(student, probe_clips: list, cfg: dict, device: torch.device,
                   k: int, bootstrap_iter: int) -> dict:
    """Embed probe clips → Prec@K / mAP@K / Cycle@K with BCa 95% CI.

    Reuses utils.bootstrap vectorized per-clip functions so numbers are
    directly comparable to the D.4 decision gate (modulo deterministic probe
    preprocessing vs m05's augmented val-time transforms).

    Forward micro-batching: AdaptiveBatchSizer wired per CLAUDE.md B35 +
    errors_N_fixes.md #47 (universal infra, no hardcoded sub_batch). Sizer
    params come from pipeline.yaml — same keys m05 uses:
      - initial_size = gpu.inference_vjepa_initial_bs (safe start)
      - max_size     = gpu.inference_adapted_bs (cap — guards CPU queue + recompile)
      - memory_cap   = gpu.gpu_memory_target (universal 0.85)
    Forward-only (@torch.no_grad), so OOM recovery is clean: shrink → retry
    same micro, no partial-grad state to discard.

    Pooling matches m05:242 — outputs.mean(dim=1) over patch dim → (B, D=1664).
    Returns {"prec_at_k": {mean, ci_half, ci_lo, ci_hi}, ..., "num_clips": N}.
    """
    from utils.bootstrap import (
        bootstrap_ci, encode_tags_to_labels,
        per_clip_prec_at_k, per_clip_map_at_k, per_clip_cycle_at_k,
    )

    n = len(probe_clips)
    if n < 10:
        # Fail-loud: stable BCa CI needs N>=10. Caller wraps in try/except and
        # logs a WARN. Previously returned a dict with None values — that let
        # trajectory stats silently degrade. No defaults, no fallback.
        raise ValueError(
            f"probe N={n} too small for stable 95% BCa CI (needs N>=10). "
            f"Check probe.subset JSON clip_keys list + probe.local_data TAR coverage."
        )

    _gpu = get_pipeline_config()["gpu"]
    # iter11 perf fix — start at max_size (not initial_bs). The ramp from 8→44 caused
    # ~37 torch.compile recompiles per probe (~30 s each = 18 min/probe × 10 probes/ep =
    # 3 h wasted per epoch on v2 POC log, 2026-04-24). Starting at max=44 hits the
    # training-side compile cache (training forward already used shape = adapted_bs).
    # OOM fallback in the while-loop below ramps DOWN via sizer.on_oom() if surprise OOM.
    sizer = AdaptiveBatchSizer(
        initial_size=_gpu["inference_adapted_bs"],   # = max_size; skip ramp
        min_size=1,
        max_size=_gpu["inference_adapted_bs"],
        memory_cap=_gpu["gpu_memory_target"],
    )
    print(f"  [probe] AdaptiveBatchSizer: start={sizer.size}, "
          f"max={sizer.max_size}, cap={sizer.memory_cap:.0%} (iter11 no-ramp)")

    was_training = student.training
    had_hier = getattr(student, "return_hierarchical", None)
    student.eval()
    if had_hier is not None:
        student.return_hierarchical = False

    try:
        model_dtype = next(student.parameters()).dtype
        tags = [t for _, t, _ in probe_clips]
        emb_list = []
        i = 0
        print(f"  [probe] encoding {n} clips...", flush=True)
        while i < n:
            end = min(i + sizer.size, n)
            batch = torch.stack([c for _, _, c in probe_clips[i:end]])
            batch = batch.permute(0, 2, 1, 3, 4).to(device=device, dtype=model_dtype)
            try:
                out = student(batch)
                emb_list.append(out.mean(dim=1).float().cpu().numpy())
                sizer.after_batch_success()
                i = end
            except torch.cuda.OutOfMemoryError:
                cuda_cleanup()
                if not sizer.on_oom():
                    raise RuntimeError(
                        f"Probe OOM at min sub-batch={sizer.min_size} (N={n}). "
                        f"Student + activations don't fit even at BS=1 on this GPU. "
                        f"Reduce inference_vjepa_initial_bs in pipeline.yaml or run "
                        f"probe on larger hardware."
                    ) from None
                # retry same i at shrunken sizer.size
        embeddings = np.concatenate(emb_list, axis=0)
    finally:
        if had_hier is not None:
            student.return_hierarchical = had_hier
        if was_training:
            student.train()

    # L2-normalize: FAISS IndexFlatL2 on normalized vecs ≡ cosine rank.
    norms = np.linalg.norm(embeddings, axis=1, keepdims=True) + 1e-8
    embeddings = (embeddings / norms).astype(np.float32)

    # N=1K: IndexFlatL2 is exact + fastest; skip IVF. CPU index fine for this size.
    import faiss
    index = faiss.IndexFlatL2(embeddings.shape[1])
    index.add(embeddings)
    _D, I = index.search(embeddings, k + 1)  # col 0 = self

    label_arrays = encode_tags_to_labels(tags, ["scene_type"])
    prec_scores = per_clip_prec_at_k(I, tags, k, field="scene_type", label_arrays=label_arrays)
    map_scores = per_clip_map_at_k(I, tags, k, field="scene_type", label_arrays=label_arrays) * 100
    cycle_scores = per_clip_cycle_at_k(I, k) * 100

    return {
        "num_clips": n,
        "prec_at_k": bootstrap_ci(prec_scores, n_boot=bootstrap_iter),
        "map_at_k": bootstrap_ci(map_scores, n_boot=bootstrap_iter),
        "cycle_at_k": bootstrap_ci(cycle_scores, n_boot=bootstrap_iter),
    }


@torch.no_grad()
def run_probe_val_loss(student, teacher, predictor, probe_clips: list,
                       mask_generators: list, cfg: dict,
                       device: torch.device) -> dict:
    """Compute JEPA val-loss on the decoded probe clips (optimization-trajectory companion
    to run_probe_eval's downstream retrieval metrics). Same masked-token L1 + dense-context
    loss used during training, evaluated on held-out val_1k clips. Cheap (~6 s at N=1000).

    Contract: student's return_hierarchical MUST be True during this call (predictor needs
    hierarchical output). Caller-safe: train() / eval() toggled and restored.

    Returns {"jepa_loss", "masked_loss", "context_loss", "n_val_clips"}.
    """
    n = len(probe_clips)
    if n < 10:
        raise ValueError(f"probe val-loss N={n} too small for stable mean")

    was_training = student.training
    had_hier = getattr(student, "return_hierarchical", None)
    student.eval()
    if had_hier is not None:
        student.return_hierarchical = True   # predictor needs hierarchical output
    teacher_was_training = teacher.training
    predictor_was_training = predictor.training
    teacher.eval()
    predictor.eval()

    _gpu = get_pipeline_config()["gpu"]
    # iter11 perf fix — see run_probe_eval note above. Same reasoning applies to
    # probe-val-loss: skip ramp; start at max=adapted_bs; OOM retry ramps down if needed.
    sizer = AdaptiveBatchSizer(
        initial_size=_gpu["inference_adapted_bs"],   # = max_size; skip ramp
        min_size=1, max_size=_gpu["inference_adapted_bs"],
        memory_cap=_gpu["gpu_memory_target"])

    mp_cfg = cfg["mixed_precision"]
    dtype = getattr(torch, mp_cfg["dtype"])
    loss_exp = cfg["optimization"]["loss_exp"]
    embed_dim = cfg["model"]["embed_dim"]
    n_levels = cfg["model"]["n_output_distillation"]
    predict_all = cfg["model"]["predict_all"]

    total_j, total_m, total_c, n_seen = 0.0, 0.0, 0.0, 0

    try:
        i = 0
        while i < n:
            end = min(i + sizer.size, n)
            micro_bs = end - i
            batch = torch.stack([c for _, _, c in probe_clips[i:end]])
            batch = batch.permute(0, 2, 1, 3, 4).to(device=device, dtype=dtype)
            try:
                all_menc, all_mpred = [], []
                for mg in mask_generators:
                    me, mp = mg(micro_bs)
                    all_menc.append(me.to(device))
                    all_mpred.append(mp.to(device))
                with torch.amp.autocast("cuda", dtype=dtype, enabled=mp_cfg["enabled"]):
                    h = teacher(batch)
                    if h.size(-1) == n_levels * embed_dim:
                        chunks = [F.layer_norm(h[:, :, lvl * embed_dim:(lvl + 1) * embed_dim], (embed_dim,))
                                  for lvl in range(n_levels)]
                        h = torch.cat(chunks, dim=2)
                    else:
                        h = F.layer_norm(h, (h.size(-1),))
                    pf, pc = [], []
                    for k, (me, mp) in enumerate(zip(all_menc, all_mpred)):
                        z = student(batch, masks=[me])
                        out = predictor(z, [me], [mp], mask_index=k)
                        if isinstance(out, tuple) and len(out) == 2:
                            pf.append(out[0]); pc.append(out[1])
                        else:
                            pf.append(out)
                    jl, lm, lc = compute_jepa_loss(pf, pc, h, all_mpred, all_menc,
                                                   loss_exp, predict_all, lambda_context=0.5)
                total_j += jl.item() * micro_bs
                total_m += lm.item() * micro_bs
                total_c += lc.item() * micro_bs
                n_seen += micro_bs
                sizer.after_batch_success()
                i = end
            except torch.cuda.OutOfMemoryError:
                cuda_cleanup()
                if not sizer.on_oom():
                    raise RuntimeError(
                        f"probe val-loss OOM at min sub-batch={sizer.min_size}"
                    ) from None
    finally:
        if had_hier is not None:
            student.return_hierarchical = had_hier
        if was_training:
            student.train()
        if teacher_was_training:
            teacher.train()
        if predictor_was_training:
            predictor.train()

    return {
        "jepa_loss": total_j / max(n_seen, 1),
        "masked_loss": total_m / max(n_seen, 1),
        "context_loss": total_c / max(n_seen, 1),
        "n_val_clips": n_seen,
    }


def compute_trajectory_stats(probe_history: list) -> dict:
    """Trajectory stats across stage boundaries.

    Single-probe-set regime: BWT degenerates to net improvement R[-1]-R[0] on
    Prec@K. Real GEM-BWT (per-task eval) would need separate D_L/D_A/D_I probe
    splits — deferred as future extension; this already flags regressions.

    Forgetting monitor: max_drop > 0 means some stage transition DROPPED Prec@K
    despite replay → the paper claim "replay prevents forgetting" fails on this
    run. User should inspect per-stage mode_mixture / warmup_pct.
    """
    # Fail-loud on structural issues — every probe_history entry must have the
    # full metric dict (run_probe_eval always returns it; missing = bug upstream).
    # Empty / single-entry history is a legit no-trajectory case (returns empty).
    if len(probe_history) < 2:
        return {"bwt_prec_at_k": None, "monotonic": None, "trajectory": []}

    traj = [r["prec_at_k"]["mean"] for r in probe_history]
    deltas = [traj[i] - traj[i - 1] for i in range(1, len(traj))]
    max_drop = -min(deltas) if min(deltas) < 0 else 0.0
    return {
        "bwt_prec_at_k": round(traj[-1] - traj[0], 4),
        "max_drop_prec_at_k": round(max_drop, 4),
        "monotonic": all(d >= 0 for d in deltas),
        "trajectory": [round(v, 4) for v in traj],
    }


