def build_optimizer(student, predictor, cfg_opt: dict, init_params: dict = None):
    """Build AdamW optimizer. Reads `use_8bit_optim` flag (#56) — when True, uses
    bitsandbytes `AdamW8bit` for 4× optimizer-state memory reduction (m1+m2 stored
    as 8-bit block-wise quantized values instead of fp32). <1% accuracy loss per
    Dettmers et al. 2022 ("8-bit Optimizers via Block-wise Quantization",
    arXiv:2110.02861). Standard in HF Trainer (`optim="adamw_bnb_8bit"`) and FAIR
    fairscale for fine-tuning 2B+ models on consumer GPUs.

    iter14 recipe-v3 intervention #4 (2026-05-09): when cfg_opt["spd"]["enabled"]
    is True and init_params is provided, returns SPDAdamW (selective projection
    decay) instead of bare AdamW — replaces uniform L2 anchor (compute_drift_loss)
    with per-param anchor pull-back gated by step-vs-anchor alignment. SPD is
    incompatible with use_8bit_optim (would need bitsandbytes wrapper); FATALs if
    both enabled. Default: spd.enabled=False → legacy AdamW path bit-identical.
    See iter/iter14_surgery_on_pretrain/plan_surgery_wins.md §4 #4 + §12.3.
    """
    base_lr = cfg_opt["lr"]
    pred_lr = base_lr * cfg_opt["pred_lr_multiplier"]
    wd = cfg_opt["weight_decay"]
    use_8bit = cfg_opt["use_8bit_optim"]
    # SPD config — strict access (no .get default per CLAUDE.md no-DEFAULT rule).
    # surgery_base.yaml provides the {enabled: false, alpha: 0.0} default block.
    spd_cfg = cfg_opt["spd"]
    spd_enabled = spd_cfg["enabled"]

    def split_params(module, lr):
        decay, no_decay = [], []
        for name, p in module.named_parameters():
            if not p.requires_grad:
                continue
            if "bias" in name or "norm" in name or "ln" in name:
                no_decay.append(p)
            else:
                decay.append(p)
        return [
            {"params": decay, "lr": lr, "weight_decay": wd},
            {"params": no_decay, "lr": lr, "weight_decay": 0.0},
        ]

    param_groups = split_params(student, base_lr) + split_params(predictor, pred_lr)

    if use_8bit:
        if spd_enabled:
            sys.exit("❌ FATAL: optimizer.spd.enabled=true is incompatible with "
                     "optimizer.use_8bit_optim=true (no bitsandbytes-SPD wrapper). "
                     "Disable one or implement SPDAdamW8bit subclass.")
        import bitsandbytes as bnb
        paged_optim = cfg_opt["paged_optim"]
        if paged_optim:
            opt = bnb.optim.PagedAdamW8bit(param_groups,
                                             betas=tuple(cfg_opt["betas"]),
                                             eps=cfg_opt["eps"])
            optim_name = "PagedAdamW8bit (bitsandbytes, unified-memory CPU paging)"
        else:
            opt = bnb.optim.AdamW8bit(param_groups,
                                       betas=tuple(cfg_opt["betas"]),
                                       eps=cfg_opt["eps"])
            optim_name = "AdamW8bit (bitsandbytes)"
        n_trainable = sum(len(g["params"]) for g in param_groups)
        print(f"  Optimizer: {optim_name} — {n_trainable} param groups, "
              f"8-bit block-wise quantized m1/m2 (4× memory reduction)")
        return opt

    if spd_enabled:
        if init_params is None:
            sys.exit("❌ FATAL: optimizer.spd.enabled=true requires init_params "
                     "(pretrain anchor) — caller must pass build_optimizer(..., "
                     "init_params=<dict>). m09c does this when --init-from-ckpt loads.")
        from utils.spd_optimizer import SPDAdamW
        opt = SPDAdamW(
            param_groups,
            anchor_state_dict=init_params,
            anchor_param_names=list(student.named_parameters())
                                + list(predictor.named_parameters()),
            alpha_spd=spd_cfg["alpha"],
            betas=tuple(cfg_opt["betas"]),
            eps=cfg_opt["eps"],
        )
        n_trainable = sum(len(g["params"]) for g in param_groups)
        print(f"  Optimizer: SPDAdamW (alpha_spd={spd_cfg['alpha']}, "
              f"n_anchored={opt._n_anchored}) — {n_trainable} param groups")
        return opt
    opt = torch.optim.AdamW(param_groups, betas=tuple(cfg_opt["betas"]),
                              eps=cfg_opt["eps"])
    n_trainable = sum(len(g["params"]) for g in param_groups)
    print(f"  Optimizer: AdamW (fp32, vanilla — SPD off) — {n_trainable} param groups")
    return opt


def enable_gradient_checkpointing(model):
    """Wrap each transformer block's forward in `torch.utils.checkpoint.checkpoint`
    for activation-memory savings (#56). Activations recomputed during backward
    instead of stored → ~2-4× reduction in activation memory at cost of ~25-30%
    throughput (one extra forward pass per block during backward).

    Uses `use_reentrant=False` (modern PyTorch API, required for torch.compile
    compatibility + proper handling of keyword args — V-JEPA blocks accept
    `mask`, `T`, `H_patches`, `W_patches`, `return_attn`, `mode` as kwargs).

    Works for BOTH trainable and frozen blocks — frozen blocks still consume
    activation memory during forward (autograd needs them to backprop gradient
    to earlier trainable blocks), so checkpointing helps regardless of
    `requires_grad`. Gold-standard pattern used by HF Transformers
    `gradient_checkpointing_enable()` and FAIR vissl's checkpoint_wrapper.

    Must be called AFTER model.train() is set and BEFORE build_optimizer —
    safe to call once per stage (idempotent via `_gradient_checkpointing` attr).
    """
    if getattr(model, "_gradient_checkpointing", False):
        return  # idempotent — already wrapped
    if not hasattr(model, "blocks"):
        raise ValueError(f"enable_gradient_checkpointing: model {type(model).__name__} "
                         f"has no .blocks attribute (expected nn.ModuleList)")

    import torch.utils.checkpoint as ckpt

    def make_ckpt_wrapper(original_forward):
        def checkpointed_forward(*args, **kwargs):
            return ckpt.checkpoint(original_forward, *args,
                                   use_reentrant=False, **kwargs)
        return checkpointed_forward

    for block in model.blocks:
        block.forward = make_ckpt_wrapper(block.forward)

    model._gradient_checkpointing = True
    print(f"  Gradient checkpointing enabled on {len(model.blocks)} blocks "
          f"(~2-4× activation memory reduction, ~25% throughput cost)")


def build_scheduler(optimizer, cfg_opt: dict, total_steps: int):
    # iter13 (2026-05-05): single-knob warmup. `warmup_pct` is a fraction in
    # [0, 1]; warmup_steps computed at runtime as `int(total_steps * warmup_pct)`.
    # Was dual-knob (warmup_steps absolute + warmup_cap_pct percent cap) which let
    # yamls hardcode warmup_steps=1500 that got silently dominated by the cap and
    # never noticed. Convention matches surgery.warmup_pct + probe_action CLI.
    warmup_pct = cfg_opt["warmup_pct"]
    if not 0.0 <= warmup_pct <= 1.0:
        sys.exit(f"FATAL: warmup_pct must be in [0, 1] (fraction); got {warmup_pct}. "
                 f"Did you mean {warmup_pct/100} (e.g., 0.12 for 12%)?")
    warmup_steps = max(1, int(total_steps * warmup_pct))
    min_lr = cfg_opt["min_lr"]
    base_lr = cfg_opt["lr"]

    def lr_lambda(step):
        if step < warmup_steps:
            return step / max(warmup_steps, 1)
        progress = (step - warmup_steps) / max(total_steps - warmup_steps, 1)
        return max(min_lr / base_lr, 0.5 * (1 + np.cos(np.pi * progress)))

    return torch.optim.lr_scheduler.LambdaLR(optimizer, lr_lambda)


def update_weight_decay(optimizer, cfg_opt: dict, step: int, total_steps: int):
    """Cosine weight decay schedule (matches Meta's CosineWDSchedule).

    Ramps WD from weight_decay to final_weight_decay over training.
    If both are equal (default: 0.04), this is a no-op.
    """
    initial_wd = cfg_opt["weight_decay"]
    final_wd = cfg_opt["final_weight_decay"]
    if initial_wd == final_wd:
        return  # no-op: fixed WD
    progress = step / max(total_steps, 1)
    new_wd = final_wd + 0.5 * (initial_wd - final_wd) * (1 + np.cos(np.pi * progress))
    for group in optimizer.param_groups:
        if group.get("weight_decay", 0) > 0:  # don't touch no_decay groups
            group["weight_decay"] = new_wd


# ═════════════════════════════════════════════════════════════════════════
# VALIDATION (JEPA L1 loss on held-out val clips)
# ═════════════════════════════════════════════════════════════════════════

def run_validation(student, teacher, predictor, mask_generators,
                   val_batches: list, cfg: dict, device: torch.device,
                   step: int, *,
                   val_keys: list = None,
                   mt_head=None, mt_dims_spec: dict = None,
                   mt_labels_by_clip: dict = None, mt_cfg: dict = None,
                   init_params: dict = None, drift_cfg: dict = None,
                   ma_head=None, ma_lookup: dict = None,
                   ma_cfg: dict = None) -> dict:
    """Compute val losses on val clips — same metrics as training loss.

    iter13 (2026-05-05): extended from "JEPA only" to all 4 components, mirroring
    training (_train_step_grad_accum):
      - val_jepa:       L1 reconstruction loss on masked tokens (always)
      - val_multi_task: 16-dim CE+BCE on taxonomy labels (when mt_head provided)
      - val_drift:      lambda · ||θ - θ_init||² (data-independent, computed once
                        per val cycle when init_params + drift_cfg provided)
      - val_total:      α·jepa + β·mt + λ·drift (yaml weights from
                        cfg.optimization.loss + cfg.drift_control)
    Backward-compat: keyword args default None → only val_jepa is computed,
    others = 0.0. Caller pulls `result["jepa"]` for the legacy scalar-tracking
    code paths (kill-switch, best-ckpt, plateau).

    Args:
        val_keys:           Flat list of clip_keys aligned with val_batches
                            (i-th batch's keys = val_keys[sum_bs_so_far : ...]).
                            Required when mt_head is provided.
        mt_head:            MultiTaskProbeHead (unmasked-features path).
        mt_dims_spec:       16-dim taxonomy spec dict.
        mt_labels_by_clip:  {clip_key → {dim_name → label}}.
        mt_cfg:             Multi-task yaml block (provides weight_probe + weight_per_dim).
        init_params:        Dict of CPU-clones of student params at start of training.
        drift_cfg:          Drift-control yaml block (provides lambda_reg).

    Returns dict with keys: jepa, multi_task, drift, motion_aux, total, n_batches.

    iter13 v13 FIX-5 (2026-05-07): added ma_head/ma_lookup/ma_cfg kwargs +
    per-batch motion_aux compute mirroring multi_task — closes the same
    asymmetry that D6-FIX closed for mt. Without this, motion_aux trains
    invisibly at val time → a NaN'd head shows up only in train step records,
    not in val plots.
    """
    student.eval()
    predictor.eval()

    mp_cfg = cfg["mixed_precision"]
    dtype = getattr(torch, mp_cfg["dtype"])
    loss_exp = cfg["optimization"]["loss_exp"]
    total_jepa = 0.0
    total_mt = 0.0
    total_ma = 0.0
    n_batches = 0
    n_batches_ma = 0
    n_seen_clips = 0   # cursor into val_keys for per-batch slicing

    # Lazy imports to avoid circular: multi_task_loss + motion_aux_loss import
    # from training-adjacent paths.
    if mt_head is not None:
        from utils.multi_task_loss import compute_multi_task_probe_loss
    if ma_head is not None:
        from utils.motion_aux_loss import compute_motion_aux_loss

    with torch.no_grad():
        for batch_clips in val_batches:
            batch_clips = batch_clips.to(device)
            actual_bs = batch_clips.shape[0]
            # Slice the per-batch keys (only needed for multi-task; harmless otherwise).
            batch_keys = (val_keys[n_seen_clips:n_seen_clips + actual_bs]
                          if val_keys is not None else None)
            n_seen_clips += actual_bs

            # Generate masks (same as training)
            all_masks_enc, all_masks_pred = [], []
            for mg in mask_generators:
                m_enc, m_pred = mg(actual_bs)
                all_masks_enc.append(m_enc.to(device))
                all_masks_pred.append(m_pred.to(device))

            with torch.amp.autocast("cuda", dtype=dtype, enabled=mp_cfg["enabled"]):
                # Teacher: all tokens + per-chunk LayerNorm (deep supervision)
                embed_dim_val = cfg["model"]["embed_dim"]
                n_levels_val = cfg["model"]["n_output_distillation"]
                # iter13 Fix #3: pass training=True / mod="video" to match Meta's
                # reference call style (deps/vjepa2/app/vjepa_2_1/train.py:593+618).
                # Semantically equivalent to return_hierarchical=True attribute path.
                h = teacher(batch_clips, training=True)
                if h.size(-1) == n_levels_val * embed_dim_val:
                    chunks = []
                    for lvl in range(n_levels_val):
                        chunk = h[:, :, lvl * embed_dim_val : (lvl + 1) * embed_dim_val]
                        chunks.append(F.layer_norm(chunk, (embed_dim_val,)))
                    h = torch.cat(chunks, dim=2)
                else:
                    h = F.layer_norm(h, (h.size(-1),))

                # Student + Predictor per mask generator
                predict_all_val = cfg["model"]["predict_all"]
                pred_features = []
                pred_context_val = []
                for i, (m_enc, m_pred) in enumerate(zip(all_masks_enc, all_masks_pred)):
                    z = student(batch_clips, masks=[m_enc], training=True)
                    outputs = predictor(z, [m_enc], [m_pred], mod="video", mask_index=i)
                    if isinstance(outputs, tuple) and len(outputs) == 2:
                        pred_features.append(outputs[0])
                        pred_context_val.append(outputs[1])
                    else:
                        pred_features.append(outputs)

                # Dense loss (same as training, no drift control)
                jepa_loss, _, _ = compute_jepa_loss(
                    pred_features, pred_context_val, h,
                    all_masks_pred, all_masks_enc,
                    loss_exp, predict_all_val, lambda_context=0.5)

                # iter13: multi-task forward on UNMASKED features (one extra
                # student forward per batch, ~10% val time overhead). Mirrors
                # the train-time pattern at multi_task_loss.py:run_multi_task_step
                # (toggle return_hierarchical OFF, mean-pool last layer, run head).
                # Skipped when mt_head/labels not provided — pure-JEPA fast path.
                # iter13 v13 FIX-5 (2026-05-07): merged mt + motion_aux unmasked
                # forward into ONE student call when both heads are active —
                # halves the eval-time forward cost (was 2 separate forwards).
                # Skipped when neither head is provided → pure-JEPA fast path
                # preserved.
                need_unmasked = (
                    (mt_head is not None and mt_labels_by_clip is not None and batch_keys)
                    or (ma_head is not None and ma_lookup is not None and batch_keys)
                )
                if need_unmasked:
                    had_hier_un = getattr(student, "return_hierarchical", None)
                    if had_hier_un is True:
                        student.return_hierarchical = False
                    try:
                        feats_unmasked = student(batch_clips)
                        if isinstance(feats_unmasked, (list, tuple)):
                            feats_unmasked = feats_unmasked[-1]
                        pooled = feats_unmasked.mean(dim=1)              # (B, D)
                        if (mt_head is not None
                                and mt_labels_by_clip is not None
                                and batch_keys):
                            mt_loss_t, _ = compute_multi_task_probe_loss(
                                pooled, mt_head, batch_keys,
                                mt_labels_by_clip, mt_dims_spec,
                                weight_per_dim=mt_cfg["weight_per_dim"],
                                device=device)
                            total_mt += float(mt_loss_t.item())
                        if (ma_head is not None
                                and ma_lookup is not None
                                and batch_keys):
                            ma_loss_t, ma_branches = compute_motion_aux_loss(
                                pooled, ma_head, batch_keys, ma_lookup,
                                weight_ce=ma_cfg["weight_ce"] if ma_cfg else 1.0,
                                weight_mse=ma_cfg["weight_mse"] if ma_cfg else 1.0,
                                device=device)
                            if ma_branches.get("n_kept", 0) > 0:
                                total_ma += float(ma_loss_t.item())
                                n_batches_ma += 1
                    finally:
                        if had_hier_un is not None:
                            student.return_hierarchical = had_hier_un

            total_jepa += jepa_loss.item()
            n_batches += 1

    val_jepa = total_jepa / max(n_batches, 1)
    val_mt   = total_mt   / max(n_batches, 1)
    val_ma   = total_ma   / max(n_batches_ma, 1)

    # iter13: drift loss is data-INDEPENDENT (||θ-θ_init||²), so compute once
    # post-loop instead of per-batch. Matches the train-time semantics at
    # _train_step_grad_accum's once-per-macro drift add.
    val_drift = 0.0
    if (init_params is not None and drift_cfg is not None
            and drift_cfg["enabled"] and drift_cfg["lambda_reg"] > 0):
        with torch.no_grad():
            val_drift = float(
                compute_drift_loss(student, init_params, drift_cfg["lambda_reg"]).item())

    # iter13 v13 FIX-5 / iter14 (2026-05-08): canonical total via compute_total_loss
    # — single source of truth shared with m09a/m09c step_record builders.
    val_total = compute_total_loss(
        jepa=val_jepa, drift=val_drift,
        mt=val_mt, mt_cfg=mt_cfg, ma=val_ma, ma_cfg=ma_cfg)

    print(f"  Step {step} | Val JEPA loss: {val_jepa:.4f} "
          f"(mt={val_mt:.4f} · ma={val_ma:.4f} · drift={val_drift:.6f} · "
          f"total={val_total:.4f}; {len(val_batches)} batches)")

    student.train()
    predictor.train()
    # Post-val cache hygiene: PyTorch's caching allocator retains freed val
    # blocks (mem_get_info reports them as "used" → AdaptiveBatchSizer reads
    # >85% post-val and shrinks training sub-batch from 8→1, ETA 7h→14h, see
    # logs/probe_train_pretrain_full_v1.log:99-111). cuda_cleanup() returns the
    # cached pool to the driver so the sizer sees true free memory next step.
    cuda_cleanup()
    return {
        "jepa":       val_jepa,
        "multi_task": val_mt,
        "motion_aux": val_ma,
        "drift":      val_drift,
        "total":      val_total,
        "n_batches":  n_batches,
    }


