@torch.no_grad()
def run_probe_acc_eval(student, probe_clips: list, probe_labels: dict,
                       cfg: dict, device: torch.device, bootstrap_iter: int) -> dict:
    """Embed probe clips → top-1 action accuracy via leave-one-out kNN-centroid.

    Per iter13: this is the PROBE-ACCURACY metric (Meta SSv2 attentive-probe
    family), NOT Prec@K (retrieval). Use for mid-training observability — does
    the encoder still produce features that cluster by action class?

    Algorithm (cheap — no probe head training):
      1. Forward probe clips through student → (N, D) mean-pooled features
      2. L2-normalize features
      3. For each clip i: compute per-class centroid from features[j != i, y[j] == c]
         for each class c; predict argmax cosine(feat_i, centroids)
      4. Bootstrap 95 % BCa CI on per-clip {0,1} correctness.

    Args:
        student: encoder (eval-mode toggled internally; restored on exit)
        probe_clips: list of (clip_key, tag_dict, tensor[T,C,H,W]) from build_probe_clips
        probe_labels: dict {clip_key → {"class_id": int, "class": str, "split": str}}
                      from utils/action_labels.load_action_labels(action_labels.json)
        bootstrap_iter: BCa iterations (typically cfg["probe"]["bootstrap_iter"] = 10000)

    Returns: {"num_clips", "num_classes", "top1_acc": {mean, ci_lo, ci_hi, ci_half}}
    """
    from utils.bootstrap import bootstrap_ci

    n = len(probe_clips)
    if n < 10:
        raise ValueError(
            f"probe N={n} too small for stable 95% BCa CI (needs N>=10). "
            f"Check probe.subset JSON clip_keys + action_labels coverage."
        )

    # Forward path mirrors run_probe_eval's batched no-ramp AdaptiveBatchSizer.
    _gpu = get_pipeline_config()["gpu"]
    sizer = AdaptiveBatchSizer(
        initial_size=_gpu["training_adapted_probe_bs"],
        min_size=1,
        max_size=_gpu["training_adapted_probe_bs"],
        memory_cap=_gpu["gpu_memory_target"],
    )

    was_training = student.training
    had_hier = getattr(student, "return_hierarchical", None)
    student.eval()
    if had_hier is not None:
        student.return_hierarchical = False

    try:
        model_dtype = next(student.parameters()).dtype
        emb_list = []
        i = 0
        print(f"  [probe-acc] encoding {n} clips...", flush=True)
        while i < n:
            end = min(i + sizer.size, n)
            batch = torch.stack([c for _, _, c in probe_clips[i:end]])
            batch = batch.permute(0, 2, 1, 3, 4).to(device=device, dtype=model_dtype)
            try:
                out = student(batch)
                emb_list.append(out.mean(dim=1).float().cpu().numpy())
                sizer.after_batch_success()
                i = end
            except torch.cuda.OutOfMemoryError:
                cuda_cleanup()
                if not sizer.on_oom():
                    raise RuntimeError(
                        f"Probe-acc OOM at min sub-batch={sizer.min_size} (N={n})."
                    ) from None
        embeddings = np.concatenate(emb_list, axis=0)
    finally:
        if had_hier is not None:
            student.return_hierarchical = had_hier
        if was_training:
            student.train()

    # L2-normalize → cosine sim becomes a dot product.
    norms = np.linalg.norm(embeddings, axis=1, keepdims=True) + 1e-8
    feats = (embeddings / norms).astype(np.float32)                  # (N, D)

    # Look up labels in clip-key order. Skip clips without a label record
    # (probe is best-effort signal — no fail-loud on missing labels).
    keys = [k for k, _, _ in probe_clips]
    y_list, keep_idx = [], []
    for idx, k in enumerate(keys):
        if k in probe_labels:
            y_list.append(int(probe_labels[k]["class_id"]))
            keep_idx.append(idx)
    if len(y_list) < 10:
        raise ValueError(
            f"probe-acc: only {len(y_list)}/{n} probe clips have action labels — "
            f"check that --probe-subset clip_keys overlap with action_labels.json"
        )
    y = np.asarray(y_list, dtype=np.int64)
    feats = feats[keep_idx]                                          # (N_labeled, D)
    n_labeled = len(y)
    classes = np.unique(y)
    n_classes = len(classes)

    # Leave-one-out per-class centroid kNN. For each clip i: build centroids
    # from features[j != i] grouped by class, then predict argmax cosine sim.
    # Vectorized — build (N, C) cosine-sim matrix using LOO centroids.
    sum_per_class = np.stack([feats[y == c].sum(axis=0) for c in classes])           # (C, D)
    cnt_per_class = np.array([(y == c).sum() for c in classes], dtype=np.float64)    # (C,)
    sims = np.zeros((n_labeled, n_classes), dtype=np.float64)
    for c_idx, c in enumerate(classes):
        is_self = (y == c).astype(np.float64)                          # (N,) — 1 if clip i ∈ class c
        cnt = cnt_per_class[c_idx] - is_self                          # (N,) — denom for each i
        # Defensive: cnt > 0 since len(probe_clips) >= 10 → every class has >= 2 clips typically.
        cnt = np.where(cnt > 0, cnt, 1.0)
        sub = feats * is_self[:, None]                                # (N, D) — feats[i] if i ∈ c else 0
        centroids = (sum_per_class[c_idx][None, :] - sub) / cnt[:, None]  # (N, D) LOO centroid
        cn = np.linalg.norm(centroids, axis=1, keepdims=True) + 1e-8
        centroids = centroids / cn
        sims[:, c_idx] = (feats * centroids).sum(axis=1)              # cos(feat_i, centroid_c@i)
    pred = classes[sims.argmax(axis=1)]
    correct = (pred == y).astype(np.float64)

    # Post-probe cache hygiene: see note in run_validation. Probe-acc forward
    # pass leaves freed activations in PyTorch's caching allocator; without
    # cleanup, AdaptiveBatchSizer reads inflated VRAM% and shrinks training.
    cuda_cleanup()
    return {
        "num_clips":   n_labeled,
        "num_classes": int(n_classes),
        "top1_acc":    bootstrap_ci(correct, n_boot=bootstrap_iter),
    }


@torch.no_grad()
def run_probe_val_loss(student, teacher, predictor, probe_clips: list,
                       mask_generators: list, cfg: dict,
                       device: torch.device, *,
                       mt_head=None, mt_dims_spec: dict = None,
                       mt_labels_by_clip: dict = None, mt_cfg: dict = None,
                       init_params: dict = None, drift_cfg: dict = None,
                       ma_head=None, ma_lookup: dict = None,
                       ma_cfg: dict = None) -> dict:
    """Compute JEPA + (optional) multi-task + (optional) drift val-loss on probe clips.

    iter13 v13 D6-FIX (2026-05-07): extended from JEPA-only to all 4 components,
    mirroring run_validation. Surgery (m09c) uses probe_clips for val (rather
    than a separate val_batches dataloader), so this function is the m09c
    counterpart to run_validation's m09a path. Both paths return the same
    4-key dict — `multi_task_loss`, `drift_loss`, `total_loss` — instead of
    forcing m09c to hardcode 0.0.

    Args (kwargs, all default None → JEPA-only fast path back-compat):
        mt_head, mt_dims_spec, mt_labels_by_clip, mt_cfg: multi-task probe inputs.
        init_params, drift_cfg: drift-control inputs (data-INDEPENDENT, computed
            once post-loop).

    Contract: student's return_hierarchical MUST be True during this call (predictor needs
    hierarchical output). Caller-safe: train() / eval() toggled and restored.

    Returns {"jepa_loss", "masked_loss", "context_loss", "n_val_clips",
             "multi_task_loss", "motion_aux_loss", "drift_loss", "total_loss"}.

    iter13 v13 FIX-5 (2026-05-07): added ma_head/ma_lookup/ma_cfg kwargs +
    per-batch motion_aux compute mirroring multi_task. Closes asymmetry
    where surgery's primary aux loss (motion_aux) was invisible at val time.
    """
    n = len(probe_clips)
    if n < 10:
        raise ValueError(f"probe val-loss N={n} too small for stable mean")

    was_training = student.training
    had_hier = getattr(student, "return_hierarchical", None)
    student.eval()
    if had_hier is not None:
        student.return_hierarchical = True   # predictor needs hierarchical output
    teacher_was_training = teacher.training
    predictor_was_training = predictor.training
    teacher.eval()
    predictor.eval()

    _gpu = get_pipeline_config()["gpu"]
    # iter11 perf fix — see run_probe_eval note above. Same reasoning applies to
    # probe-val-loss: skip ramp; start at max=training_adapted_probe_bs (=24, not 44).
    # OOM retry ramps down if surprise OOM.
    sizer = AdaptiveBatchSizer(
        initial_size=_gpu["training_adapted_probe_bs"],   # = max_size; skip ramp
        min_size=1, max_size=_gpu["training_adapted_probe_bs"],
        memory_cap=_gpu["gpu_memory_target"])

    mp_cfg = cfg["mixed_precision"]
    dtype = getattr(torch, mp_cfg["dtype"])
    loss_exp = cfg["optimization"]["loss_exp"]
    embed_dim = cfg["model"]["embed_dim"]
    n_levels = cfg["model"]["n_output_distillation"]
    predict_all = cfg["model"]["predict_all"]

    total_j, total_m, total_c, n_seen = 0.0, 0.0, 0.0, 0
    total_mt = 0.0   # multi-task per-batch (averaged at end)
    n_batches_mt = 0
    total_ma = 0.0   # motion_aux per-batch (averaged at end)
    n_batches_ma = 0

    # Lazy imports to avoid circular: multi_task_loss + motion_aux_loss import
    # from training-adjacent paths.
    if mt_head is not None:
        from utils.multi_task_loss import compute_multi_task_probe_loss
    if ma_head is not None:
        from utils.motion_aux_loss import compute_motion_aux_loss

    try:
        i = 0
        while i < n:
            end = min(i + sizer.size, n)
            micro_bs = end - i
            # iter13 v13 D6-FIX: capture clip_keys for multi-task label lookup.
            batch_keys = [k for k, _, _ in probe_clips[i:end]]
            batch = torch.stack([c for _, _, c in probe_clips[i:end]])
            batch = batch.permute(0, 2, 1, 3, 4).to(device=device, dtype=dtype)
            try:
                all_menc, all_mpred = [], []
                for mg in mask_generators:
                    me, mp = mg(micro_bs)
                    all_menc.append(me.to(device))
                    all_mpred.append(mp.to(device))
                with torch.amp.autocast("cuda", dtype=dtype, enabled=mp_cfg["enabled"]):
                    h = teacher(batch)
                    if h.size(-1) == n_levels * embed_dim:
                        chunks = [F.layer_norm(h[:, :, lvl * embed_dim:(lvl + 1) * embed_dim], (embed_dim,))
                                  for lvl in range(n_levels)]
                        h = torch.cat(chunks, dim=2)
                    else:
                        h = F.layer_norm(h, (h.size(-1),))
                    pf, pc = [], []
                    for k, (me, mp) in enumerate(zip(all_menc, all_mpred)):
                        z = student(batch, masks=[me])
                        out = predictor(z, [me], [mp], mask_index=k)
                        if isinstance(out, tuple) and len(out) == 2:
                            pf.append(out[0]); pc.append(out[1])
                        else:
                            pf.append(out)
                    jl, lm, lc = compute_jepa_loss(pf, pc, h, all_mpred, all_menc,
                                                   loss_exp, predict_all, lambda_context=0.5)

                    # iter13 v13 D6/FIX-5: shared UNMASKED forward feeds mt + motion_aux
                    # heads. Halves eval-time forward cost when both are active. Skipped
                    # when neither head is provided → pure-JEPA fast path preserved.
                    need_unmasked = (
                        (mt_head is not None and mt_labels_by_clip is not None and batch_keys)
                        or (ma_head is not None and ma_lookup is not None and batch_keys)
                    )
                    if need_unmasked:
                        had_hier_un = getattr(student, "return_hierarchical", None)
                        if had_hier_un is True:
                            student.return_hierarchical = False
                        try:
                            feats_unmasked = student(batch)
                            if isinstance(feats_unmasked, (list, tuple)):
                                feats_unmasked = feats_unmasked[-1]
                            pooled = feats_unmasked.mean(dim=1)              # (B, D)
                            if (mt_head is not None
                                    and mt_labels_by_clip is not None
                                    and batch_keys):
                                mt_loss_t, _ = compute_multi_task_probe_loss(
                                    pooled, mt_head, batch_keys,
                                    mt_labels_by_clip, mt_dims_spec,
                                    weight_per_dim=mt_cfg["weight_per_dim"],
                                    device=device)
                                total_mt += float(mt_loss_t.item())
                                n_batches_mt += 1
                            if (ma_head is not None
                                    and ma_lookup is not None
                                    and batch_keys):
                                ma_loss_t, ma_branches = compute_motion_aux_loss(
                                    pooled, ma_head, batch_keys, ma_lookup,
                                    weight_ce=ma_cfg["weight_ce"] if ma_cfg else 1.0,
                                    weight_mse=ma_cfg["weight_mse"] if ma_cfg else 1.0,
                                    device=device)
                                if ma_branches.get("n_kept", 0) > 0:
                                    total_ma += float(ma_loss_t.item())
                                    n_batches_ma += 1
                        finally:
                            if had_hier_un is not None:
                                student.return_hierarchical = had_hier_un
                total_j += jl.item() * micro_bs
                total_m += lm.item() * micro_bs
                total_c += lc.item() * micro_bs
                n_seen += micro_bs
                sizer.after_batch_success()
                i = end
            except torch.cuda.OutOfMemoryError:
                cuda_cleanup()
                if not sizer.on_oom():
                    raise RuntimeError(
                        f"probe val-loss OOM at min sub-batch={sizer.min_size}"
                    ) from None
    finally:
        if had_hier is not None:
            student.return_hierarchical = had_hier
        if was_training:
            student.train()
        if teacher_was_training:
            teacher.train()
        if predictor_was_training:
            predictor.train()
        # Post-eval cache hygiene (see run_validation note). Inside `finally`
        # so it runs even on exception/OOM.
        cuda_cleanup()

    val_jepa = total_j / max(n_seen, 1)
    val_mt = total_mt / max(n_batches_mt, 1)
    val_ma = total_ma / max(n_batches_ma, 1)

    # iter13 v13 D6-FIX: drift loss is data-INDEPENDENT (||θ-θ_init||²), so
    # compute once post-loop instead of per-batch. Mirrors run_validation:889-897.
    val_drift = 0.0
    if (init_params is not None and drift_cfg is not None
            and drift_cfg["enabled"] and drift_cfg["lambda_reg"] > 0):
        with torch.no_grad():
            val_drift = float(
                compute_drift_loss(student, init_params, drift_cfg["lambda_reg"]).item())

    weight_mt = float(mt_cfg["weight_probe"]) if mt_cfg is not None else 0.0
    weight_ma = float(ma_cfg["weight_motion"]) if ma_cfg is not None else 0.0
    val_total = val_jepa + weight_mt * val_mt + weight_ma * val_ma + val_drift

    return {
        "jepa_loss": val_jepa,
        "masked_loss": total_m / max(n_seen, 1),
        "context_loss": total_c / max(n_seen, 1),
        "n_val_clips": n_seen,
        "multi_task_loss": val_mt,
        "motion_aux_loss": val_ma,
        "drift_loss": val_drift,
        "total_loss": val_total,
    }


def compute_trajectory_stats(probe_history: list) -> dict:
    """Trajectory stats across stage boundaries — keyed on probe_top1.

    iter13 v13 (2026-05-07): switched from legacy retrieval prec_at_k to
    motion-flow probe_top1 (the iter13 paper-grade gate metric). Single-probe-set
    regime: BWT degenerates to net top-1 improvement R[-1]-R[0]. Forgetting
    monitor: max_drop > 0 means some stage transition DROPPED top-1 despite
    replay → "replay prevents forgetting" claim fails on this run.

    Returns empty trajectory when <2 probe entries OR no entry has probe_top1
    (caller handles both gracefully — surgery may have probe disabled).
    """
    if len(probe_history) < 2:
        return {"bwt_top1": None, "monotonic": None, "trajectory": []}

    traj = [r.get("probe_top1") for r in probe_history if r.get("probe_top1") is not None]
    if len(traj) < 2:
        return {"bwt_top1": None, "monotonic": None, "trajectory": []}

    deltas = [traj[i] - traj[i - 1] for i in range(1, len(traj))]
    max_drop = -min(deltas) if min(deltas) < 0 else 0.0
    return {
        "bwt_top1": round(traj[-1] - traj[0], 4),
        "max_drop_top1": round(max_drop, 4),
        "monotonic": all(d >= 0 for d in deltas),
        "trajectory": [round(v, 4) for v in traj],
    }


