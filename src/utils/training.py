def save_training_checkpoint(path: Path, student, teacher, predictor,
                              optimizer, scheduler, scaler,
                              step: int, best_metric: float,
                              full: bool = True, uw=None,
                              include_optimizer: bool = True):
    """Save checkpoint.

    iter11 disk-budget fix (2026-04-24, after POC v3 disk-full crash at step 6):
    teacher + predictor were previously ALWAYS included. For a ViT-G 2B model:
      student    ≈ 4 GB (bf16) or 8 GB (fp32)
      teacher    ≈ 4 GB / 8 GB — deepcopy of student, only needed for EMA-resume
      predictor  ≈ 240 MB
      optimizer  ≈ 16 GB (AdamW, 2 fp32 moments × 2B params)
    With `full=True` → ~24 GB per ckpt. POC writes 5 live ckpts → 120 GB → disk-full.

    New semantics:
      full=True  → resume-capable: student + teacher + predictor + optimizer + scheduler + scaler
                   (~20-24 GB on ViT-G). Use for m09*_ckpt_latest.pt (one file, resume anchor).
      full=False → SELECTION-ONLY: student ONLY (~4 GB). Use for step*.pt, best.pt,
                   best_prec.pt — they're read only to pick the "best" student and export it;
                   teacher/predictor are rebuilt on resume via student EMA init anyway.

    iter13 disk-budget fix (2026-05-04, after v6 ENOSPC crash at step 744):
      `include_optimizer=False` (with full=True) → eval-ready ckpt with student +
      teacher + predictor (~8 GB on ViT-G), but NO optimizer/scheduler/scaler. Used
      for `_best.pt` since downstream (m05 surgical re-embed, m06 Prec@K, Stage 8
      probe_future_mse) needs only encoder + predictor — optimizer state (16 GB)
      is dead weight. Saves 22 GB durable + 22 GB transient per best.pt rewrite.
      latest.pt keeps include_optimizer=True (resume requires it).

    POC footprint after fix: 4+4 (2 steps @ keep_n=2) + 20 (latest) + 4 (best) + 4 (best_prec)
    = 36 GB peak (vs 120 GB before).
    """
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp_path = path.with_suffix(".tmp")
    ckpt = {
        "student": student.state_dict(),
        "step": step,
        "best_metric": best_metric,
        "is_full": full,                      # marker for load_training_checkpoint fallback
        "has_optimizer": full and include_optimizer,  # marker so load can dispatch
    }
    if full:
        ckpt["teacher"] = teacher.state_dict()
        ckpt["predictor"] = predictor.state_dict()
    if full and include_optimizer:
        ckpt["optimizer"] = optimizer.state_dict()
        ckpt["scheduler"] = scheduler.state_dict()
        ckpt["scaler"] = scaler.state_dict() if scaler else None
        if uw is not None:
            ckpt["uw"] = uw.state_dict()
    # Atomic write (tmp -> rename). On torch.save failure (almost always disk-full
    # at ViT-G ~24 GB ckpt scale), clean up the partial .tmp so disk does not keep
    # filling on retry, and re-raise with disk-free context. errors_N_fixes.md
    # (2026-05-03 disk-full at step 86 left m09a_ckpt_latest.tmp at 24 GB).
    try:
        torch.save(ckpt, tmp_path)
        os.replace(tmp_path, path)
    except (RuntimeError, OSError) as e:
        if tmp_path.exists():
            try:
                tmp_path.unlink()
            except OSError as _unlink_err:
                print(f"  WARN: could not remove partial {tmp_path}: {_unlink_err}")
        free_gb = -1.0
        try:
            free_gb = shutil.disk_usage(path.parent).free / 1e9
        except OSError as _du_err:
            print(f"  WARN: disk_usage probe failed: {_du_err}")
        kind = "full=True (~24GB)" if full else "full=False (~7GB)"
        raise RuntimeError(
            f"save_training_checkpoint FAILED writing {path.name} ({kind}); "
            f"disk free: {free_gb:.1f} GB at {path.parent.resolve()}. "
            f"Underlying: {type(e).__name__}: {e}"
        ) from e


def cleanup_old_checkpoints(output_dir: Path, prefix: str, keep_n: int = 2,
                             cache_policy: str = "1"):
    """Delete oldest {prefix}_step*.pt files, keeping only the last keep_n.

    iter11 v3 (2026-04-26): UNCONDITIONAL `keep_last_n` rotation. Step ckpts
    are training scratch (resume points) — they get superseded every
    `saves_per_epoch` save. Once a newer ckpt exists, older ones are dead
    weight. At m09b's saves_per_epoch=5 cadence × 5 epochs = 25 step ckpts
    × ~10 GB each = ~250 GB pile-up if eviction is gated.
    Reserve cache_policy for catastrophic deletes (whole dirs, m05 .npy,
    frozen baselines) — NOT routine training-scratch rotation.
    cache_policy arg retained for backward-compat; ignored.

    `prefix` MUST match the caller module's CHECKPOINT_PREFIX (e.g. "m09b_ckpt")
    — previously this was hardcoded to the utils-module CHECKPOINT_PREFIX
    ("m09_ckpt"), causing a silent zero-match glob for m09b (writes "m09b_ckpt_*")
    and m09c (writes "m09c_ckpt_*"), which piled up ckpts until disk-full.
    """
    del cache_policy  # iter11 v3: deliberately ignored — see docstring
    pattern = str(output_dir / f"{prefix}_step*.pt")
    step_files = sorted(glob.glob(pattern),
                        key=lambda f: os.path.getmtime(f))
    if len(step_files) > keep_n:
        for old_file in step_files[:-keep_n]:
            Path(old_file).unlink()
            print(f"  [keep_last_n={keep_n}] rotated out step ckpt: {Path(old_file).name}")


def cleanup_stage_checkpoints(output_dir: Path, prefix: str, keep_n: int = 1,
                               cache_policy: str = "1"):
    """Delete oldest `{prefix}_stage*.pt` files, keeping only the last `keep_n`.

    Mirror of `cleanup_old_checkpoints` but for **stage-suffixed** saves
    (m09c surgery writes stage0 / stage1 / stage2 — not step-N). Keeps disk
    footprint at `keep_n × ~15 GB` instead of cumulative `3 × ~15 GB`.

    Usage in m09c:
      - Mid-training (per-stage save): `keep_n=1` → one resume anchor on disk.
      - Post-export (after `export_student_for_eval`): `keep_n=0` → student_encoder.pt
        is the only artifact downstream needs, stage rollback points are disposable.

    The glob is sorted by mtime so the N most-recent writes survive regardless of
    stage ordering (important if a resume re-writes stage2 with a newer timestamp
    than stage0/1 — we keep the "newest save", not the "highest stage index").
    """
    pattern = str(output_dir / f"{prefix}_stage*.pt")
    stage_files = sorted(glob.glob(pattern),
                         key=lambda f: os.path.getmtime(f))
    if len(stage_files) > keep_n:
        target = stage_files[:-keep_n] if keep_n > 0 else stage_files
        # iter11 META-fix: gate destructive stage-ckpt cleanup through cache_policy.
        # Default=1/keep is safe; pass cache_policy=args.cache_policy from orchestrator.
        from utils.cache_policy import guarded_delete
        for old_file in target:
            guarded_delete(old_file, cache_policy,
                           label="m09c stage checkpoint")


