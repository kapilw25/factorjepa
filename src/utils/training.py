def set_trainable_prefix(student, n_layers: int):
    """Freeze all blocks, then unfreeze blocks [0, n_layers). Rebuild optimizer after calling."""
    for param in student.parameters():
        param.requires_grad = False
    for i in range(min(n_layers, len(student.blocks))):
        for param in student.blocks[i].parameters():
            param.requires_grad = True
    for name, param in student.named_parameters():
        if "norm" in name or "ln" in name:
            param.requires_grad = True
    trainable = sum(p.numel() for p in student.parameters() if p.requires_grad)
    total = sum(p.numel() for p in student.parameters())
    print(f"  Trainable prefix: {n_layers}/{len(student.blocks)} blocks "
          f"({trainable:,}/{total:,} = {100*trainable/total:.1f}%)")


def create_train_val_split(clip_keys, split_ratio: float, seed: int,
                           max_val_clips: int = 1000):
    """Deterministic train/val split via seeded shuffle. Reproducible across runs
    with same (keys, split_ratio, seed, max_val_clips). Used by m09c to carve a
    held-out val-set from the factor manifest so the same set powers mid-training
    probe + downstream Step F decision gate — eliminates the 2026-04-19 test-leakage flaw.

    val_size = min(max_val_clips, int(n * (1 - split_ratio))). Cap prevents FULL
    (115K clips × 10% = 11500) from burning GPU on an over-sized val-set when 1000
    clips already give tight BCa CIs. POC (1000 × 10% = 100) is below cap → 100 val.

    split_ratio=1.0 returns (all_keys, []) — SANITY uses this (N=20 too small to split).
    """
    if split_ratio >= 1.0:
        return list(clip_keys), []
    rng = random.Random(seed)
    shuffled = list(clip_keys)
    rng.shuffle(shuffled)
    n_total = len(shuffled)
    # Avoid `n_total * (1 - split_ratio)` — IEEE 754 rounds 1-0.9 = 0.0999... → off-by-one.
    # Compute n_train first (exact for round multiples), derive n_val from it, then cap.
    n_train = int(n_total * split_ratio)
    n_val = min(max_val_clips, n_total - n_train)
    n_train = n_total - n_val   # re-derive so cap actually increases n_train on FULL
    return shuffled[:n_train], shuffled[n_train:]


def build_factor_index(manifest: dict, dl_dir: Path, da_dir: Path, di_dir: Path) -> dict:
    """Build clip_key → {D_L: path, D_A: path, D_I: [paths]} from m11 manifest."""
    index = {}
    for clip_key, info in manifest.items():
        safe_key = clip_key.replace("/", "__")
        entry = {}
        if info["has_D_L"]:
            p = dl_dir / f"{safe_key}.npy"
            if p.exists():
                entry["D_L"] = p
        if info["has_D_A"]:
            p = da_dir / f"{safe_key}.npy"
            if p.exists():
                entry["D_A"] = p
        if info.get("has_D_I") and info.get("n_interaction_tubes", 0) > 0:
            tubes = sorted(di_dir.glob(f"{safe_key}_tube*.npy"))
            if tubes:
                entry["D_I"] = tubes
        if entry:
            index[clip_key] = entry
    print(f"  Factor index: {len(index)} clips "
          f"(D_L={sum(1 for e in index.values() if 'D_L' in e)}, "
          f"D_A={sum(1 for e in index.values() if 'D_A' in e)}, "
          f"D_I={sum(1 for e in index.values() if 'D_I' in e)})")
    return index


class FactorSampler:
    """Sample factor clips according to per-stage mode mixture weights."""

    def __init__(self, factor_index: dict, mode_mixture: dict):
        factor_map = {"L": "D_L", "A": "D_A", "I": "D_I"}
        self.clips_by_factor = {}
        for fk in ["D_L", "D_A", "D_I"]:
            self.clips_by_factor[fk] = [
                (key, entry[fk])
                for key, entry in factor_index.items()
                if fk in entry
            ]
        self.factors = []
        self.weights = []
        for mode_key, weight in mode_mixture.items():
            if weight <= 0:
                continue
            fk = factor_map[mode_key]
            if not self.clips_by_factor[fk]:
                counts = {k: len(v) for k, v in self.clips_by_factor.items()}
                raise ValueError(
                    f"FactorSampler: mode_mixture requests {mode_key}={weight} "
                    f"but clips_by_factor[{fk}] is empty (counts={counts}). "
                    f"Silent renormalization banned per CLAUDE.md §5. Fix: regenerate "
                    f"{fk} upstream OR set {mode_key}=0.0 in yaml mode_mixture."
                )
            self.factors.append(fk)
            self.weights.append(weight)
        if not self.factors:
            raise ValueError(
                f"FactorSampler: no positive weights in mode_mixture={mode_mixture}"
            )
        total_w = sum(self.weights)
        self.weights = [w / total_w for w in self.weights]
        requested = {k: v for k, v in mode_mixture.items() if v > 0}
        actual = dict(zip(self.factors, self.weights))
        print(
            f"  FactorSampler: REQUESTED={requested} → "
            f"ACTUAL={{{', '.join(f'{f}={w:.0%}' for f, w in actual.items())}}}"
        )

    def sample(self) -> tuple:
        """Returns (factor_type, clip_key, path_or_paths)."""
        factor = np.random.choice(self.factors, p=self.weights)
        clips = self.clips_by_factor[factor]
        idx = np.random.randint(len(clips))
        clip_key, path = clips[idx]
        if isinstance(path, list):
            path = path[np.random.randint(len(path))]
        return factor, clip_key, path


